package S7015532_calcarea;

class Polygon{
  protected double base, height;

  protected void setBase(double base) {
    this.base = base;
  }
  protected void setHeight(double height) {
    this.height = height;
  }

  protected double getBase() {
    return base;
  }
  protected double getHeight() {
    return height;
  }
}

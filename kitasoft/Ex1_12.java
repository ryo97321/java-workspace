package kitasoft;

public class Ex1_12 {
    public static void main(String[] args) {
        int x = 13;
        System.out.printf("x++=%d\n", x++);

        x = 13;
        System.out.printf("++x=%d\n", ++x);

        x = 13;
        System.out.printf("x--=%d\n", x--);

        x = 13;
        System.out.printf("--x=%d\n", --x);
    }
}